package ch02_var;

public class Var_Ex_6 {

	public static void main(String[] args) {
		byte b1 = -1;
		byte b2 = -10;
		byte b3 = -128;
		System.out.println(b1+": "+Integer.toBinaryString(b1));
		System.out.println(b2+": "+Integer.toBinaryString(b2));
		System.out.println(b3+": "+Integer.toBinaryString(b3));
	
	}

}
