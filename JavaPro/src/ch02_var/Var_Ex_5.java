package ch02_var;

public class Var_Ex_5 {

	public static void main(String[] args) {
		char a = '\'';
		System.out.println(a);
		// ' ', " " 사용하기 위해선 \ 역슬레시 앞에 붙여준다.
		System.out.println("\\");
		System.out.println("\\n다음줄"+"\nNext");
		System.out.println("\\r앞으로");
		System.out.println("\\t탭키만큼\t		띄우기");
		System.out.println("\'\'");
		System.out.println("\"\"");
	}

}
